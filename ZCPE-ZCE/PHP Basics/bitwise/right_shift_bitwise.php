<?php
    // *******************************************
    // *   OPERATOR   * SHORT FORM * NORMAL FORM *
    // *******************************************
    // *  RIGHT SHIFT *    \>>=    *     \>>     *
    // *******************************************

    /**
     * 00101101 => 45 (BEGIN)
     * 
     * 00010110 => 22 (I°)
     * 
     * 00001011 => 11 (II°)
     * 
     * 00000101 =>  5 (III°)
     */

    $c = 45 >> 3;                 // 5

    $d = (int) ((45) / (2 ** 3)); // 5

    print($c);

    echo "<br />";

    print($d);

    echo "<br /><br />";
?>